// Clase calculadora
class Calculadora {
  // Metodo sumar
  sumar(num1, num2) {
    return num1 + num2;
  }

  // Metodo restar
  restar(num1, num2) {
    return num1 - num2;
  }

  // Metodo dividir
  dividir(num1, num2) {
    return num1 / num2;
  }

  // Metodo multiplicar
  multiplicar(num1, num2) {
    return num1 * num2;
  }
}
